print("سلام! من اسکل‌بات هستم 😎")
print("هر چی خواستی بپرس، یا بنویس 'خداحافظ' برای خروج.")

while True:
    user_input = input("تو: ")

    if user_input.lower() in ["خداحافظ", "bye", "خدانگهدار"]:
        print("اسکل‌بات: اوکی بابا، خداحافظ 🤖✌️")
        break
    elif "خوبی" in user_input:
        print("اسکل‌بات: من همیشه خوبم، تو چطوری؟ 🧠")
    elif "چند سالته" in user_input:
        print("اسکل‌بات: من ساخته‌ی حمیدرضزام. سن ندارم، فقط هوشم! 👶🤖")
    elif "اسکل" in user_input:
        print("اسکل‌بات: اسکل خودتی 😂 ولی دوستت دارم")
    else:
        print("اسکل‌بات: نمی‌فهمم چی گفتی! بپرس یه چیز دیگه.")

print("برنامه تمام شد.")
